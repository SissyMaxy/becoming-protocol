-- ============================================
-- 5. outreach_credentials
-- ============================================
-- Encrypted at rest with the same AES-256-GCM scheme as calendar_credentials.
-- Encryption key lives in OUTREACH_TOKEN_KEY (32 bytes, base64).
-- One row per (user, platform). Today only Reddit is supported; the enum
-- leaves room for FetLife/Discord if they ever ship usable APIs.

CREATE TABLE IF NOT EXISTS outreach_credentials (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  platform outreach_platform NOT NULL,

  username TEXT,

  oauth_token_encrypted TEXT NOT NULL,
  refresh_token_encrypted TEXT NOT NULL,
  expires_at TIMESTAMPTZ NOT NULL,
  scopes TEXT[] NOT NULL DEFAULT '{}',

  connected_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  disconnected_at TIMESTAMPTZ,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

  UNIQUE (user_id, platform)
);

CREATE INDEX IF NOT EXISTS idx_outreach_credentials_active
  ON outreach_credentials(user_id, platform) WHERE disconnected_at IS NULL;

ALTER TABLE outreach_credentials ENABLE ROW LEVEL SECURITY;

DO $$ BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_policies WHERE tablename = 'outreach_credentials'
      AND policyname = 'Users own their data'
  ) THEN
    CREATE POLICY "Users own their data" ON outreach_credentials
      FOR ALL USING (auth.uid() = user_id);
  END IF;
END $$;

