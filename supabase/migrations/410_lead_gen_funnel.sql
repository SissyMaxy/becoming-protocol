-- BLOCK draft inserts for contacts whose status='blocked'.
CREATE OR REPLACE FUNCTION public.trg_block_drafts_for_blocked_contacts()
RETURNS trigger LANGUAGE plpgsql AS $function$
DECLARE
  contact_status TEXT;
BEGIN
  SELECT status INTO contact_status
    FROM maxy_contacts_crm
   WHERE id = NEW.contact_id;
  IF contact_status = 'blocked' THEN
    RAISE EXCEPTION 'outbound_draft_queue: contact % is blocked, no further drafts allowed', NEW.contact_id
      USING ERRCODE = '23514'; -- check_violation
  END IF;
  RETURN NEW;
END;
$function$;

DROP TRIGGER IF EXISTS outbound_block_blocked_contacts ON outbound_draft_queue;
CREATE TRIGGER outbound_block_blocked_contacts
  BEFORE INSERT ON outbound_draft_queue
  FOR EACH ROW EXECUTE FUNCTION trg_block_drafts_for_blocked_contacts();

