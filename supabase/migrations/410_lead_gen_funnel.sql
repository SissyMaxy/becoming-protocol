-- ============================================
-- 11. Funnel daily aggregate view
-- ============================================
-- Daily rollup: contacts → DMs → followers → paying → repeat per channel.
-- The audience-summarizer fn reads this for the weekly Mommy-voice summary.

CREATE OR REPLACE VIEW audience_funnel_daily AS
SELECT
  user_id,
  channel,
  (occurred_at AT TIME ZONE 'UTC')::date AS day,
  COUNT(*) FILTER (WHERE event_type = 'contacted')          AS contacted_count,
  COUNT(*) FILTER (WHERE event_type = 'dm_sent')             AS dms_sent_count,
  COUNT(*) FILTER (WHERE event_type = 'response_received')   AS responses_count,
  COUNT(*) FILTER (WHERE event_type = 'social_followed')     AS followers_count,
  COUNT(*) FILTER (WHERE event_type = 'content_purchased')   AS purchases_count,
  COUNT(*) FILTER (WHERE event_type = 'subscription_started')AS subs_started_count,
  COUNT(*) FILTER (WHERE event_type = 'churned')             AS churned_count,
  COUNT(*) FILTER (WHERE event_type = 'blocked')             AS blocked_count,
  COALESCE(SUM(value_cents), 0)                              AS revenue_cents
FROM audience_funnel_events
GROUP BY user_id, channel, (occurred_at AT TIME ZONE 'UTC')::date;

-- Per-channel LTV: realized revenue + projected, divided by acquired contacts.
CREATE OR REPLACE VIEW audience_channel_ltv AS
SELECT
  c.user_id,
  c.source AS channel,
  COUNT(c.id)                                          AS contacts_acquired,
  SUM(c.realized_value_cents)                          AS realized_value_cents,
  SUM(c.projected_ltv_cents)                           AS projected_ltv_cents,
  CASE WHEN COUNT(c.id) = 0 THEN 0
       ELSE SUM(c.realized_value_cents) / COUNT(c.id)
  END                                                   AS realized_ltv_per_contact_cents,
  CASE WHEN COUNT(c.id) = 0 THEN 0
       ELSE SUM(c.projected_ltv_cents) / COUNT(c.id)
  END                                                   AS projected_ltv_per_contact_cents
FROM maxy_contacts_crm c
WHERE c.status NOT IN ('blocked')
GROUP BY c.user_id, c.source;

