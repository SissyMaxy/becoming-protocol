-- ============================================
-- 2. conversion_path_templates
-- ============================================

CREATE TABLE IF NOT EXISTS conversion_path_templates (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  -- Templates are global (NOT user-scoped) — they're the protocol's playbook,
  -- not user data. The drafter personalizes per-contact at compose time.
  archetype TEXT NOT NULL CHECK (archetype IN (
    'panty_curious', 'voice_curious', 'recurring_kink',
    'paying_first_time', 'chatter_only'
  )),
  step_index SMALLINT NOT NULL CHECK (step_index BETWEEN 0 AND 10),

  -- The step's structural intent (what we want this message to accomplish).
  intent TEXT NOT NULL,
  -- A Mommy-authored prompt skeleton the drafter expands into a real DM.
  -- Includes {pet_name}, {handle}, {kink_specific} placeholders. Voice and
  -- craft are still enforced at compose time — the skeleton is structure,
  -- not a script.
  prompt_skeleton TEXT NOT NULL,

  -- Optional next-step routing. response_branch can be:
  --   'positive' / 'neutral' / 'negative' / NULL (default linear advance)
  -- The drafter classifies the contact's reply and follows the matching
  -- branch. NULL means advance to step_index+1 regardless.
  response_branch TEXT CHECK (response_branch IN ('positive', 'neutral', 'negative')),
  next_step_index SMALLINT,

  -- Conversion goal for this step: 'tease' / 'social_follow' / 'custom_offer' /
  -- 'subscription' / 'warm_chat'. Used by funnel analytics.
  conversion_goal TEXT NOT NULL CHECK (conversion_goal IN (
    'tease', 'social_follow', 'custom_offer', 'subscription', 'warm_chat'
  )),

  -- How long to wait before composing this step (after previous completed).
  cooldown_hours SMALLINT NOT NULL DEFAULT 12,

  -- Anti-pressure floor: max times this step can fire for one contact.
  max_attempts SMALLINT NOT NULL DEFAULT 1,

  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),

  UNIQUE (archetype, step_index, response_branch)
);

CREATE INDEX IF NOT EXISTS idx_conversion_templates_archetype_step
  ON conversion_path_templates (archetype, step_index);

ALTER TABLE conversion_path_templates ENABLE ROW LEVEL SECURITY;
-- Templates are global — readable by any authenticated user, writable only
-- by service role.
DROP POLICY IF EXISTS conversion_templates_read ON conversion_path_templates;
CREATE POLICY conversion_templates_read ON conversion_path_templates
  FOR SELECT TO authenticated USING (true);
DROP POLICY IF EXISTS conversion_templates_service ON conversion_path_templates;
CREATE POLICY conversion_templates_service ON conversion_path_templates
  FOR ALL TO service_role USING (true) WITH CHECK (true);

