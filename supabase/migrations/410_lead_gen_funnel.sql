-- maxy_contacts_crm), so no additional policies needed.

-- ============================================
-- 12. Approve-and-send RPC (the ONLY path to status='sent')
-- ============================================
-- Dave clicks send in the UI → UI calls this RPC with the draft id → RPC
-- transitions status to 'sent' and stamps sent_at. No edge fn has direct
-- update authority on these columns; the trigger below blocks any other
-- write path.

CREATE OR REPLACE FUNCTION public.outbound_draft_send(
  draft_id UUID
)
RETURNS outbound_draft_queue
LANGUAGE plpgsql SECURITY INVOKER
AS $function$
DECLARE
  result outbound_draft_queue%ROWTYPE;
BEGIN
  UPDATE outbound_draft_queue
     SET status = 'sent',
         sent_at = now(),
         updated_at = now()
   WHERE id = draft_id
     AND user_id = auth.uid()
     AND status IN ('awaiting_review', 'approved')
  RETURNING * INTO result;

  IF result.id IS NULL THEN
    RAISE EXCEPTION 'outbound_draft_send: draft % not found, not owned, or not in a sendable state', draft_id;
  END IF;

  RETURN result;
END;
$function$;

GRANT EXECUTE ON FUNCTION public.outbound_draft_send(UUID) TO authenticated;

