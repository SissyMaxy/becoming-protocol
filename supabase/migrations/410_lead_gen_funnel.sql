-- Migration 410 — Lead-gen + audience-building funnel.
--
-- The Sniffies-warmth → social-follower → paying-subscriber pipeline.
-- Mommy as outbound marketing director. This migration creates:
--   1. maxy_contacts_crm        — unified CRM across Sniffies/Twitter/Reddit/
--                                 Fansly/OF/manual. Tier + status + path-state.
--   2. conversion_path_templates — DM-sequence templates keyed by archetype.
--                                 Mommy-authored; voice + craft + honest-rep
--                                 enforced at draft time.
--   3. audience_funnel_events    — every state transition logged: contacted →
--                                 dm_sent → response_received → social_followed
--                                 → content_purchased → churned. Per-channel
--                                 LTV computed from these.
--   4. maxy_persona_spec         — single authoritative description of who Maxy
--                                 is. Bio, voice patterns, body, kink keywords,
--                                 content cadence, hard limits. Auto-poster +
--                                 DM drafter both cross-check.
--   5. mommy_authority_log       — cross-system Mommy-action audit (every edge
--                                 fn that takes autonomous action writes here).
--   6. outbound_draft_queue      — DRAFT outbound DMs to third parties.
--                                 STATUS DEFAULT 'awaiting_review'. NEVER
--                                 auto-sent. Dave-click-to-send only.
--   7. content_demand_signals    — what contacts ask for. Feeds
--                                 mommy-content-prompter + mommy-scene-author.
--
-- HARD FLOORS — real safety, enforced at the schema layer:
--   * outbound_draft_queue.status starts 'awaiting_review'. The only sent_at
--     write path is via UI button. No edge fn sets status='sent'.
--   * maxy_contacts_crm.status='blocked' propagates: a CHECK on
--     outbound_draft_queue prevents inserts for blocked contacts.
--   * Safety-flag tier (threats / doxxing / underage requests) → contact
--     auto-blocks (status='blocked') AND a row in handler_outreach_queue
--     surfaces the alert in plain English. Generated by the edge fn; this
--     migration only sets up the schema.
--   * Voice cleanup trigger on outbound_draft_queue.draft_text fires
--     mommy_voice_cleanup() when persona='dommy_mommy' (mirrors mig 255).
--
-- Coexistence:
--   * Sibling branches authored migrations 367 (scope-authority), 370-371
--     (voice curriculum + arousal_panel_source), 400-404 (authority wave).
--     This is 410, leaving a deliberate gap.
--   * No collisions with sniffies_contacts (mig 343), handler_outreach_queue
--     (mig 156), contact_intelligence (mig 204), outreach_post_drafts (mig 348).
--   * Cross-channel: maxy_contacts_crm.source_ref_id is a soft pointer to
--     sniffies_contacts.id when source='sniffies'. No FK — sniffies rows can
--     be excluded or deleted independently.

-- ============================================
-- 1. maxy_contacts_crm
-- ============================================

CREATE TABLE IF NOT EXISTS maxy_contacts_crm (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,

  source TEXT NOT NULL CHECK (source IN (
    'sniffies', 'twitter', 'reddit', 'fansly_dm', 'of_dm', 'manual'
  )),
  -- Soft pointer to source-system row (e.g. sniffies_contacts.id). NULL OK.
  source_ref_id UUID,
  -- Handle/username in the source system. Display label.
  source_handle TEXT NOT NULL,

  first_contact_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  last_interaction_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  last_message_excerpt TEXT,

  status TEXT NOT NULL DEFAULT 'cold' CHECK (status IN (
    'cold', 'warmed', 'follower', 'paying', 'ghost', 'blocked'
  )),
  -- 1 = lowest priority / one-liner generic, 5 = highest / paying-ready.
  value_tier SMALLINT NOT NULL DEFAULT 1 CHECK (value_tier BETWEEN 1 AND 5),

  -- 0-100 sub-scores from mommy-contact-screener. Stored for audit + tuning.
  budget_signal_score SMALLINT,
  kink_alignment_score SMALLINT,
  engagement_quality_score SMALLINT,
  safety_flag_score SMALLINT,
  conversion_likelihood_score SMALLINT,

  -- Mommy-classified archetype: panty_curious / voice_curious / recurring_kink /
  -- paying_first_time / chatter_only. Drives template selection.
  archetype TEXT CHECK (archetype IN (
    'panty_curious', 'voice_curious', 'recurring_kink',
    'paying_first_time', 'chatter_only', 'unclassified'
  )),

  -- Projected lifetime value, in cents. Updated as funnel events fire.
  projected_ltv_cents INTEGER NOT NULL DEFAULT 0,
  realized_value_cents INTEGER NOT NULL DEFAULT 0,

  -- Mommy's running notes in her own voice. Read by conversion-author when
  -- composing the next message — keeps the relationship continuous.
  mommy_notes_text TEXT,

  -- Conversion path state: which step of the archetype's template sequence
  -- the contact is currently at (0 = not started, 1+ = step index).
  conversion_path_state SMALLINT NOT NULL DEFAULT 0,

  -- When the contact auto-blocked: reason string for the user alert.
  blocked_reason TEXT,
  blocked_at TIMESTAMPTZ,

  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),

  -- One row per (user, source, source_handle). Keeps re-imports idempotent.
  UNIQUE (user_id, source, source_handle)
);

CREATE INDEX IF NOT EXISTS idx_maxy_contacts_user_status
  ON maxy_contacts_crm (user_id, status, value_tier DESC);
CREATE INDEX IF NOT EXISTS idx_maxy_contacts_user_archetype
  ON maxy_contacts_crm (user_id, archetype) WHERE archetype IS NOT NULL;
CREATE INDEX IF NOT EXISTS idx_maxy_contacts_user_last
  ON maxy_contacts_crm (user_id, last_interaction_at DESC);
CREATE INDEX IF NOT EXISTS idx_maxy_contacts_user_source
  ON maxy_contacts_crm (user_id, source);
CREATE INDEX IF NOT EXISTS idx_maxy_contacts_blocked
  ON maxy_contacts_crm (user_id, blocked_at) WHERE status = 'blocked';

