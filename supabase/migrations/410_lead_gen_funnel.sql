-- ============================================
-- 6. outbound_draft_queue
-- ============================================
-- DRAFT outbound DMs to third parties (Sniffies contacts, Twitter DMs, etc.).
-- HARD FLOOR: status starts 'awaiting_review'. Edge fns NEVER set sent_at or
-- status='sent' — only the UI button (via service-role RPC) does. The
-- separation is enforced by check constraints + a trigger that blocks any
-- direct transition from 'awaiting_review' → 'sent' from a service-role
-- write that doesn't come through the approved RPC.

CREATE TABLE IF NOT EXISTS outbound_draft_queue (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  contact_id UUID NOT NULL REFERENCES maxy_contacts_crm(id) ON DELETE CASCADE,

  -- The archetype + step the drafter used. NULL on manual-add drafts.
  archetype TEXT,
  template_step_index SMALLINT,

  -- The draft Mommy composed. Stored as-is for Dave to read.
  draft_text TEXT NOT NULL,
  -- The platform where this would be sent.
  channel TEXT NOT NULL CHECK (channel IN (
    'sniffies', 'twitter', 'reddit', 'fansly_dm', 'of_dm', 'manual'
  )),
  -- The handle/thread identifier inside that channel. Free-form;
  -- the UI uses this to render "Sending to <X>".
  channel_handle TEXT NOT NULL,

  -- Status lifecycle:
  --   awaiting_review  — Mommy wrote it, waiting for Dave.
  --   approved         — Dave clicked review → ok. (still not sent)
  --   sent             — Dave clicked send. ONLY set via approved RPC.
  --   rejected         — Dave clicked reject.
  --   expired          — Older than 7 days, never sent.
  --   rewritten        — Honest-rep gate flagged it; new draft superseded it.
  status TEXT NOT NULL DEFAULT 'awaiting_review' CHECK (status IN (
    'awaiting_review', 'approved', 'sent', 'rejected', 'expired', 'rewritten'
  )),
  -- Honest-rep gate result. NULL until reviewed by outbound-draft-review.
  -- pass / fail / rewritten
  honest_rep_status TEXT CHECK (honest_rep_status IN ('pass', 'fail', 'rewritten')),
  honest_rep_notes TEXT,
  -- Predecessor pointer when the gate rewrote a previous draft.
  rewrote_from_id UUID REFERENCES outbound_draft_queue(id) ON DELETE SET NULL,

  -- Timestamps for the lifecycle.
  drafted_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  reviewed_at TIMESTAMPTZ,
  approved_at TIMESTAMPTZ,
  sent_at TIMESTAMPTZ,
  expires_at TIMESTAMPTZ NOT NULL DEFAULT (now() + interval '7 days'),

  -- Generation audit: the persona spec hash + prompt fingerprint at draft time.
  generation_context JSONB NOT NULL DEFAULT '{}'::jsonb,

  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),

  -- HARD FLOOR: sent_at can only be set when status='sent'.
  CONSTRAINT outbound_sent_consistency CHECK (
    (status = 'sent' AND sent_at IS NOT NULL) OR
    (status <> 'sent' AND sent_at IS NULL)
  )
);

CREATE INDEX IF NOT EXISTS idx_outbound_user_status
  ON outbound_draft_queue (user_id, status, drafted_at DESC);
CREATE INDEX IF NOT EXISTS idx_outbound_contact
  ON outbound_draft_queue (contact_id, drafted_at DESC);
CREATE INDEX IF NOT EXISTS idx_outbound_pending_review
  ON outbound_draft_queue (user_id, drafted_at)
  WHERE status = 'awaiting_review';

