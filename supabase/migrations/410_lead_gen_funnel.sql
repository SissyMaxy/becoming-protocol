-- ============================================
-- 9. Mommy-voice cleanup on draft text (mirrors mig 255 pattern)
-- ============================================
-- If the user's persona is dommy_mommy, run the SQL-side cleanup on
-- draft_text before insert. Backstop for any LLM-side telemetry leak the
-- edge fn missed. Falls back to identity transform if mommy_voice_cleanup()
-- doesn't exist yet (e.g. clean install before mig 255).

CREATE OR REPLACE FUNCTION public.trg_outbound_draft_voice_cleanup()
RETURNS trigger LANGUAGE plpgsql AS $function$
DECLARE
  persona TEXT;
  has_cleanup BOOLEAN;
BEGIN
  -- Only run for dommy_mommy users.
  SELECT handler_persona INTO persona FROM user_state WHERE user_id = NEW.user_id;
  IF persona IS DISTINCT FROM 'dommy_mommy' THEN
    RETURN NEW;
  END IF;
  -- Check the cleanup function exists.
  SELECT EXISTS (
    SELECT 1 FROM pg_proc WHERE proname = 'mommy_voice_cleanup'
  ) INTO has_cleanup;
  IF has_cleanup THEN
    BEGIN
      NEW.draft_text := mommy_voice_cleanup(NEW.draft_text);
    EXCEPTION WHEN OTHERS THEN
      RAISE NOTICE 'mommy_voice_cleanup failed on draft %: %', NEW.id, SQLERRM;
    END;
  END IF;
  RETURN NEW;
END;
$function$;

DROP TRIGGER IF EXISTS outbound_draft_voice_cleanup ON outbound_draft_queue;
